/**
 * @file scare_trigger.hpp
 * @brief Абстрактный интерфейс запроса скримера.
 *
 * Это посредник между системой бустов и игровым циклом. BoostManager умеет
 * запрашивать скример (Surprise box, отравленное яблоко), но не
 * знает, как тот рисуется и как устроена игра. Класс
 * Game реализует этот интерфейс: проверяет режим, кулдаун и
 * настройку доступности, и делает что нужно с автоматом игры.
 */

#pragma once

#include <SDL3/SDL.h>

#include <string>

///@brief Интерфейс запуска скримера.
class ScareTrigger {
 public:
  virtual ~ScareTrigger() = default;

  /**
   * @brief Запрашивает показ скримера.
   *
   * Реализация сама решает, выполнить ли запрос: учитывается кулдаун
   * между скримерами, наличие щита у игрока и пользовательская настройка
   * интенсивности.
   *
   * @param[in] texture_tag  Тег полноэкранной текстуры скримера.
   * @param[in] duration_ms  Длительность показа в миллисекундах.
   */
  virtual void request_scare(const std::string& texture_tag,
                             Uint64 duration_ms) = 0;
};
