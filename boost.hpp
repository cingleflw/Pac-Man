/**
 * @file boost.hpp
 * @brief Буст на игровом поле.
 *
 * Бусты - это отдельная группа сущностей.
 * Один буст появляется на случайной достижимой игроком клетке по интервалу
 * времени (на поле не более одного буста за раз), висит какое-то
 * время и исчезает либо при подборе игроком, либо по таймауту.
 */

#pragma once

#include <string>

#include "game_object.hpp"

/**
 * @brief Тип буста.
 *
 * Числовое значение совпадает с номером кадра в спрайтлисте бустов (8 кадров в
 * первой строке), поэтому BoostType приводится к индексу кадра при отрисовке
 * через GameObject::render(). Порядок элементов менять нельзя без изменения
 * спрайтлиста.
 */
enum class BoostType : int {
  Grape = 0,        ///< Рывок - временное ускорение игрока.
  Bell = 1,         ///< Заморозка всех призраков.
  Potion = 2,       ///< Ослепление - призраки шатаются случайно, но опасны.
  Key = 3,          ///< Открывает дверь дома призраков для игрока.
  GummyBear = 4,    ///< Щит - поглощает один контакт с призраком.
  Wand = 5,         ///< Загоняет всех призраков в дом.
  SurpriseBox = 6,  ///< Случайный эффект по таблице весов (+ enrage и скример).
  Apple = 7,        ///< Плюс очки, шанс на скример (отравленное яблоко).

  Count = 8  ///< Число типов бустов.
};

/**
 * @brief Бустер на игровом поле.
 *
 * Наследует от GameObject позицию, размеры и отрисовку. Хранит свой тип,
 * по которому BoostManager выбирает эффект при подборе.
 */
class Boost : public GameObject {
 public:
  /**
   * @brief Конструктор.
   *
   * @param[in] type        Тип бустера.
   * @param[in] x           Координата X левого верхнего угла в пикселях.
   * @param[in] y           Координата Y левого верхнего угла в пикселях.
   * @param[in] size        Сторона квадратного спрайта в пикселях.
   * @param[in] texture_tag Тег спрайтлиста бустов в TextureManager.
   */
  Boost(BoostType type, float x, float y, float size,
        const std::string& texture_tag);

  /// @brief Возвращает тип буста.
  BoostType get_type() const { return type_; }

 private:
  BoostType type_;  ///< Тип буста.
};
